class fox():
	def init(self):
		self.legs = 4
		self.eyecolour = "Black"
		self.furcolour = "Orange"
		self.type = "Mammal"
	def set__legs(self,n):
		self.legs = n
	def get__legs(self):
		print("Fox has "+str(self.legs)+" legs")
	def set__eyecolour(self,colour):
		self.eyecolour = colour
	def get__eyecolour(self):
		print("Fox's eyecolour is " +str(self.eyecolour))
	def set__furcolour(self,colour):
		self.furcolour = colour
	def get__furcolour(self):
		print("Fox's fur colour is "+str(self.furcolour))
	def speak(self):
		print("Sound of fox is 'ow-wow-wow-wow'")
	def run(self):
		pass

